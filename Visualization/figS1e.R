setwd("G:/AI_XuLab_Data/Radial-C/figureS1/FigS1/FigS1e/")

aaa <- read.table("MNase-1min.bed", header = F)
colnames(aaa) <- c("fragment1", "fragment2")
png('G:/AI_XuLab_Data/Radial-C/figS1e_MNase-1min.png', width = 3, height = 3.2, units = 'in', res = 300)
par(mar = c(2.2, 2.2, 2.2, 0.3))
smoothScatter(aaa$fragment1, aaa$fragment2, nbin = 600, xlim = c(0, 1), ylim = c(0, 1), axes = F, nrpoints = 0, xlab = NA, ylab = NA)
axis(1, at = c(0, 0.5, 1), labels = c(0, 0.5, 1), cex.axis = 1, line = 0, tck = -0.01, padj = -1.2, cex.axis = 1.2)
axis(2, at = c(0, 0.5, 1), labels = c(0, 0.5, 1), cex.axis = 1, line = 0, tck = -0.01, padj = 1.2, cex.axis = 1.2)
title(main = '1D score', cex.main = 1.4, line = 0.8)
mtext('Fragment 1 (1 min)', cex = 1.3, side = 1, line = 1.15)
mtext('Fragment 2 (1 min)', cex = 1.3, side = 2, line = 1.15)
dev.off()

